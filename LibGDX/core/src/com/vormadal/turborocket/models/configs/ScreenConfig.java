package com.vormadal.turborocket.models.configs;

public class ScreenConfig {
	public float screenX, screenY, width, height;
	public ScreenConfig(float screenX, float screenY, float width, float height){
		this.screenX = screenX;
		this.screenY = screenY;
		this.width = width;
		this.height = height;
	}
}
